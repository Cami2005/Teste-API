export function somar (numero1, numero2) {
    return numero1 + numero2;
}

export function dobro (n) {
    return n * 2;
}

export function media (nota1, nota2, nota3) {
    return (nota1  + nota2 + nota3) /3;
}

export function Cores(cor) {
    cor= cor.toLowerCase();
    return cor=='amarelo' || cor=='azul' || cor=='vermelho'
}

